#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 1)
		return (0);
	i = 1;
	while (i * i < nb && i < 46341)
	{
		i++;
	}
	if (i * i == nb)
		return (i);
	return (0);
}
